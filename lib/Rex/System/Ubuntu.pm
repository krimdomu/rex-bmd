package Rex::System::Ubuntu;
   
use strict;
use warnings;

use Rex::System::Debian;

use base qw(Rex::System::Debian);

sub new {
   my $that = shift;
   my $proto = ref($that) || $that;
   my $self = $that->SUPER::new(@_);

   bless($self, $proto);

   return $self;
}

1;
